import turtle
# Vẽ bề mặt
t = turtle.Turtle()
t.speed(8)
t.pensize(2)
t.penup()
t.goto(0, -300)
t.pendown()
t.circle(300)
# Vẽ mắt trái
t.penup()
t.goto(150, 105)
t.pendown()
t.circle(25)
t.penup()
t.goto(150, 130)
t.pendown()
t.dot(20)
# Vẽ mắt phải
t.penup()
t.goto(-150, 105)
t.pendown()
t.circle(25)
t.penup()
t.goto(-150, 130)
t.pendown()
t.dot(20)
# Vẽ lông mày
t.pensize(8)
t.penup()
t.goto(180, 175)
t.pendown()
t.goto(123, 170)
t.penup()
t.goto(-180, 175)
t.pendown()
t.goto(-123, 170)
# Vẽ mũi
t.pensize(4)
t.penup()
t.goto(0, -30)
t.pendown()
t.left(10)
t.forward(350)
t.circle(15, 180)
t.goto(0, 10)
# Vẽ miệng
t.penup()
t.goto(120, -75)
t.pendown()
t.goto(-120, -75)
t.left(90)
t.circle(120, 161)
turtle.done()
